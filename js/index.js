
var image =document.getElementById ("image")
image.addEventListener("mouseover", function(){
    this.style ="box-shadow: 20px 20px 20px aqua";
    this.width = "110"

});

image.addEventListener("mouseout", function(){
    this.style ="";
    this.width = "100"

});

